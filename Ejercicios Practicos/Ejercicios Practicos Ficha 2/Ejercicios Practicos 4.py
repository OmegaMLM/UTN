a = float(input('Ingrese el valor a:'))
b = float(input('Ingrese el valor b: '))
c = float(input('Ingrese el valor c:'))
x = float(input('Ingrese el valor x:'))
res_polinomio = (a * x) ** 2 + b * x + c
discriminante = b ** 2 - 4 * a * c
print('Respuesta del polinomio: ', res_polinomio)
print('Respuesta del discriminante: ', discriminante)
